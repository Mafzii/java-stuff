// Student Class
class Student {

  private String name;
  private String rollNumber;

  public void setName(String name) {
    this.name = name;
  }

  public String getName() {
    return this.name;
  }

  public void setRollNumber(String rollNumber) {
    this.rollNumber = rollNumber;
  }

  public String getRollNumber() {
    return this.rollNumber;
  }

}